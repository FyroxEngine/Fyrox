#[cfg(test)]
mod tests {
    use super::*;
    use crate::field_reader::field_from_array;
    use ndarray::Array3;

    #[test]
    fn table_has_256_entries() {
        assert_eq!(TRIANGLE_TABLE.len(), 256);
    }

    #[test]
    fn case_0_and_255_produce_no_triangles() {
        // Case 0 = all outside, Case 255 = all inside — both produce nothing
        let data  = Array3::from_elem((4, 4, 4), 2.0_f32); // all outside
        let field = field_from_array(data, 0.5);
        let case  = cube_case(&field, 0, 0, 0);
        assert_eq!(case, 0, "all-outside field should produce case 0");
        assert!(TRIANGLE_TABLE[0].iter().all(|&v| v == -1));
        assert!(TRIANGLE_TABLE[255].iter().all(|&v| v == -1));
    }

    #[test]
    fn march_on_sphere_field_produces_triangles() {
        let res    = 12;
        let center = res as f32 / 2.0;
        let mut data = Array3::from_elem((res, res, res), 1.0_f32);
        for iz in 0..res {
            for iy in 0..res {
                for ix in 0..res {
                    let r = ((iz as f32 - center).powi(2)
                           + (iy as f32 - center).powi(2)
                           + (ix as f32 - center).powi(2)).sqrt();
                    if r < center * 0.6 {
                        data[[iz, iy, ix]] = 0.0;
                    }
                }
            }
        }
        let field = field_from_array(data, 0.5);
        let soup  = march(&field);
        assert!(!soup.triangles.is_empty(), "march should produce triangles from sphere field");
    }

    #[test]
    fn no_nan_in_march_output() {
        let res    = 8;
        let center = res as f32 / 2.0;
        let mut data = Array3::from_elem((res, res, res), 1.0_f32);
        for iz in 0..res {
            for iy in 0..res {
                for ix in 0..res {
                    let r = ((iz as f32 - center).powi(2)
                           + (iy as f32 - center).powi(2)
                           + (ix as f32 - center).powi(2)).sqrt();
                    if r < center * 0.5 { data[[iz, iy, ix]] = 0.0; }
                }
            }
        }
        let field = field_from_array(data, 0.5);
        let soup  = march(&field);
        for tri in &soup.triangles {
            for v in &tri.vertices {
                assert!(!v.x.is_nan());
                assert!(!v.y.is_nan());
                assert!(!v.z.is_nan());
            }
        }
    }

    #[test]
    fn cube_case_single_corner_inside() {
        // One corner inside — should give case 1 (bit 0 set)
        let mut data = Array3::from_elem((4, 4, 4), 1.0_f32);
        data[[0, 0, 0]] = 0.0; // corner 0 of cube at [0,0,0]
        let field = field_from_array(data, 0.5);
        let case  = cube_case(&field, 0, 0, 0);
        assert_eq!(case, 1, "single inside corner should give case 1");
    }
}
