/// marching_cubes.rs — Marching Cubes iso-surface extraction.
///
/// Fallback algorithm when Propagating Contours finds no valid seeds.
/// Also used for ground-truth validation.
///
/// From Acropora manual (§1.2):
///   "A density volume is divided into regular small cubes (voxels) where the
///    scalar value is determined at each corner of the cube. A set of rules
///    define how specific polygons can be generated within the cube if an
///    iso-value passes between adjacent corners. With over 255 possibilities,
///    a single cube can generate from 0 to 5 triangles."
///
/// Corner numbering convention (standard Lorensen & Cline 1987):
///
///     7 ---- 6
///    /|      /|
///   4 ---- 5  |
///   |  3 --|- 2
///   | /    | /
///   0 ---- 1
///
///   Corner 0: (ix,   iy,   iz  )
///   Corner 1: (ix+1, iy,   iz  )
///   Corner 2: (ix+1, iy+1, iz  )
///   Corner 3: (ix,   iy+1, iz  )
///   Corner 4: (ix,   iy,   iz+1)
///   Corner 5: (ix+1, iy,   iz+1)
///   Corner 6: (ix+1, iy+1, iz+1)
///   Corner 7: (ix,   iy+1, iz+1)
///
/// Edge numbering:
///   0: c0-c1   1: c1-c2   2: c2-c3   3: c3-c0
///   4: c4-c5   5: c5-c6   6: c6-c7   7: c7-c4
///   8: c0-c4   9: c1-c5  10: c2-c6  11: c3-c7

use crate::field_reader::ScalarField;
use crate::mesh::{Triangle, TriangleSoup};
use glam::Vec3;

// ---------------------------------------------------------------------------
// Corner offsets — [dz, dy, dx] per corner index
// ---------------------------------------------------------------------------

const CORNER_OFFSETS: [[usize; 3]; 8] = [
    [0, 0, 0], // 0
    [0, 0, 1], // 1
    [0, 1, 1], // 2
    [0, 1, 0], // 3
    [1, 0, 0], // 4
    [1, 0, 1], // 5
    [1, 1, 1], // 6
    [1, 1, 0], // 7
];

/// Edge endpoints: each edge connects two corner indices.
const EDGE_CORNERS: [[usize; 2]; 12] = [
    [0, 1], [1, 2], [2, 3], [3, 0],
    [4, 5], [5, 6], [6, 7], [7, 4],
    [0, 4], [1, 5], [2, 6], [3, 7],
];

// ---------------------------------------------------------------------------
// Cube case determination
// ---------------------------------------------------------------------------

/// Compute the 8-bit Marching Cubes case index for a voxel cube.
///
/// Bit i is set if corner i is inside the surface (value ≤ iso_threshold).
/// Result is 0–255, indexing into TRIANGLE_TABLE.
#[inline]
pub fn cube_case(field: &ScalarField, iz: usize, iy: usize, ix: usize) -> u8 {
    let mut case = 0u8;
    for (i, &[dz, dy, dx]) in CORNER_OFFSETS.iter().enumerate() {
        let val = field.get(
            (iz + dz) as isize,
            (iy + dy) as isize,
            (ix + dx) as isize,
        );
        if val <= field.iso_threshold {
            case |= 1 << i;
        }
    }
    case
}

// ---------------------------------------------------------------------------
// Edge interpolation
// ---------------------------------------------------------------------------

/// Linearly interpolate the iso-crossing position along an edge.
///
/// Returns the world-space position where the iso-threshold crosses
/// between corner `a` and corner `b`.
#[inline]
fn interpolate_edge(
    field: &ScalarField,
    iz:    usize,
    iy:    usize,
    ix:    usize,
    edge:  usize,
) -> Vec3 {
    let [ca, cb] = EDGE_CORNERS[edge];
    let [dza, dya, dxa] = CORNER_OFFSETS[ca];
    let [dzb, dyb, dxb] = CORNER_OFFSETS[cb];

    let pa = field.voxel_corner(iz + dza, iy + dya, ix + dxa);
    let pb = field.voxel_corner(iz + dzb, iy + dyb, ix + dxb);

    let va = field.get((iz + dza) as isize, (iy + dya) as isize, (ix + dxa) as isize);
    let vb = field.get((iz + dzb) as isize, (iy + dyb) as isize, (ix + dxb) as isize);

    // Guard against degenerate edges where va == vb
    let dv = vb - va;
    if dv.abs() < f32::EPSILON {
        return pa.lerp(pb, 0.5);
    }

    let t = (field.iso_threshold - va) / dv;
    pa.lerp(pb, t.clamp(0.0, 1.0))
}

// ---------------------------------------------------------------------------
// Triangulate a single cube
// ---------------------------------------------------------------------------

/// Generate triangles for a single voxel cube and push them into the soup.
///
/// Uses the precomputed `case` value (from cube_case()) and the TRIANGLE_TABLE
/// to determine which edges to interpolate.
pub fn triangulate_cube(
    field: &ScalarField,
    iz:    usize,
    iy:    usize,
    ix:    usize,
    case:  u8,
    soup:  &mut TriangleSoup,
) {
    let tri_indices = &TRIANGLE_TABLE[case as usize];

    let mut i = 0;
    while i < tri_indices.len() {
        // -1 sentinel means end of triangle list for this case
        if tri_indices[i] == -1 { break; }

        // Safety: a non-sentinel entry always starts a complete 3-index triple.
        // The table is padded to 16 so the previous sentinel check fires before
        // we can ever read past the end of the 16-element array.
        let e0 = tri_indices[i]     as usize;
        let e1 = tri_indices[i + 1] as usize;
        let e2 = tri_indices[i + 2] as usize;

        let v0 = interpolate_edge(field, iz, iy, ix, e0);
        let v1 = interpolate_edge(field, iz, iy, ix, e1);
        let v2 = interpolate_edge(field, iz, iy, ix, e2);

        // Skip degenerate triangles
        let edge_a = v1 - v0;
        let edge_b = v2 - v0;
        let normal = edge_a.cross(edge_b);
        if normal.length_squared() < f32::EPSILON { i += 3; continue; }

        soup.push(Triangle {
            vertices: [v0, v1, v2],
            normal:   normal.normalize(),
        });

        i += 3;
    }
}

// ---------------------------------------------------------------------------
// Brute force full-volume march
// ---------------------------------------------------------------------------

/// Run Marching Cubes over the entire field.
///
/// Evaluates every cube — all res³ voxels. Slow for large fields but
/// guaranteed to find the surface regardless of seed point quality.
/// Used as fallback when Propagating Contours finds no seeds.
pub fn march(field: &ScalarField) -> TriangleSoup {
    let res  = field.resolution as usize;
    let mut soup = TriangleSoup::new();

    // We march cube [iz..iz+1, iy..iy+1, ix..ix+1] — so last index is res-1
    for iz in 0..res.saturating_sub(1) {
        for iy in 0..res.saturating_sub(1) {
            for ix in 0..res.saturating_sub(1) {
                let case = cube_case(field, iz, iy, ix);
                if case == 0 || case == 255 { continue; } // no surface here
                triangulate_cube(field, iz, iy, ix, case, &mut soup);
            }
        }
    }

    soup
}

